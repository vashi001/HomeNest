import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);
// timestamp uses for sorting of items and when ever user is created or updated the time stamp gets changed

const User = mongoose.model("User", userSchema);

export default User;
